package com.cat.bean;

public class ISFReportMachine {
    private String macId;

    private String macName;

    private String macModel;

    private Double macNum;

    public String getMacId() {
        return macId;
    }

    public void setMacId(String macId) {
        this.macId = macId;
    }

    public String getMacName() {
        return macName;
    }

    public void setMacName(String macName) {
        this.macName = macName;
    }

    public String getMacModel() {
        return macModel;
    }

    public void setMacModel(String macModel) {
        this.macModel = macModel;
    }

    public Double getMacNum() {
        return macNum;
    }

    public void setMacNum(Double macNum) {
        this.macNum = macNum;
    }
}